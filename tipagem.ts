//declaração de variáveis
//var, let, const

let x =10;
console.log(x);

const y = 20;
// const = constante, ou seja, não permite mudar o valor da variável
console.log(y);

//Tipagem de Variáveis
//number, string, boolean, number[], string[]
let z:number = 30;
console.log(z);

let s:string = "Vívian";
console.log(s);

let a:any = 40;
a = "Lucas";
console.log(a);